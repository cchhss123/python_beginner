# 整數
x = 10
print(type(x))  # <class 'int'>

# 浮點數
y = 3.14
print(type(y))  # <class 'float'>

# 字串
z = "Hello"
print(type(z))  # <class 'str'>

# 布林值
a = True
print(type(a))  # <class 'bool'>

# 列表
b = [1, 2, 3]
print(type(b))  # <class 'list'>

# 元組
c = (1, 2, 3)
print(type(c))  # <class 'tuple'>

# 字典
d = {'name': 'John', 'age': 30}
print(type(d))  # <class 'dict'>

# 集合
e = {1, 2, 3}
print(type(e))  # <class 'set'>